def get_cam2view(date):
    dateint = int(date.split('_')[-1])
    if dateint<805:
        view2cam = oldview2cam
    else:
        view2cam = newview2cam
    cam2view = {}
    for scene, v2c in view2cam.items():
        c2v = {}
        for k, v in v2c.items():
            c2v[v] = k
        cam2view[scene] = c2v
    return cam2view